bil1 = int(input("Masukkan bilangan pertama: "))

bil2 = int(input("Masukkan bilangan kedua: "))

hasil_penjumlahan = bil1 + bil2

hasil_pengurangan = bil1 - bil2

hasil_modulus = bil1 % bil2

print("Hasil penjumlahan adalah", hasil_penjumlahan)
print("Hasil pengurangan adalah", hasil_pengurangan)
print("Hasil modulus adalah", hasil_modulus)